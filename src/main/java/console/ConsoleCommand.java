package console;

/**
 * コンソールで入力できるコマンドを表す列挙型。
 *
 * 文字列をそのまま比較し続けると、タイプミスや修正漏れが起きやすい。
 * そこで、コマンドの種類をenumとしてまとめて管理する。
 */
public enum ConsoleCommand {

    /** Enterキーによる1ステップ実行。 */
    STEP,

    /** ブレークポイントまで連続実行する。 */
    RUN,

    /** ブレークポイントを追加する。 */
    BREAK,

    /** ブレークポイントを削除する。 */
    DELETE,

    /** ブレークポイント一覧を表示する。 */
    BREAKS,

    /** ブレークポイントをすべて削除する。 */
    CLEAR,

    /** 実行を終了する。 */
    QUIT,

    /** 不明なコマンド。 */
    UNKNOWN
}